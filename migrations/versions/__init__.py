class OrphanedDataBase:
    """Base class for orphaned data cleanup.

    Provides base functionality for finding and removing orphaned data
    from database tables.

    Attributes:
        __tablename__: Name of the table to clean.
        find_orphaned_data_sql: SQL query to find orphaned records.
        related_tables: List of related tables to check.

    Methods:
        get: Fetch orphaned record IDs from the database.
        remove: Delete orphaned records by their IDs.

    Generated by Duo
    """

    __tablename__ = None
    find_orphaned_data_sql = None
    related_tables: ClassVar[list[str]] = []

    @classmethod
    def get(cls, conn: Connection):
        """
        Fetch ids from sql query
        Args:
            conn: Connection

        Returns:
            list of ids

        """
        # check itself too
        cls.related_tables = [cls.__tablename__, *cls.related_tables]
        # ensure all tables are exising in sqlite master
        for table in cls.related_tables:
            inspector = sa.inspect(conn)
            table_existing = inspector.has_table(table)
            if not table_existing:
                return []
        return conn.execute(sa.text(cls.find_orphaned_data_sql)).scalars().fetchall()

    @classmethod
    def remove(cls, conn: Connection, broken_ids: Sequence[int]):
        """
        Drop all records from table by broken_ids
        Args:
            conn: Connection
            broken_ids:
        """
        for broken_id in broken_ids:
            conn.execute(sa.text(f'DELETE FROM {cls.__tablename__} WHERE id = {broken_id}'))


