def log_execution_time(prefix='', logging_exception=True):
    """Decorator to log function run time
    Arguments:
        fn {function} -- [description]
        prefix {string} -- prefix set to logged message
    Returns:
        fn {function} -- [description]
    """

    def decorator(fn):
        @wraps(fn)
        def wrapper(*args: Any, **kwargs: Mapping[str, Any]):
            start_dt = datetime.now()
            start = perf_counter()
            try:
                result = fn(*args, **kwargs)
            except Exception as e:
                logger.exception(e)
                raise e
            finally:
                print_log_with_processing_time(start, start_dt)
            return result

        def print_log_with_processing_time(start, start_dt):
            end = perf_counter()
            count_time = end - start
            log_func = logger.info if count_time >= 1 else logger.debug
            log_func(
                '{log_prefix} Function: {function}; START: {start_time}; ExecTime: {exec_time:.6f}s',
                log_prefix=prefix,
                function=fn.__name__,
                start_time=start_dt,
                exec_time=count_time,
            )

        return wrapper

    return decorator
