    def connect(self):
        dsn = f'host={self.host:s} '
        dsn += f'port={int(self.port):d} '
        dsn += f'dbname={self.dbname:s} '
        dsn += f'user={self.username:s} '
        dsn += f'password={self.password:s}'
        try:
            self.connection = psycopg2.connect(dsn)
            cur = self.connection.cursor()
            self.is_connected = True

            if self.schema:
                cur.execute(
                    f"SELECT schema_name FROM information_schema.schemata WHERE schema_name = '{self.schema}';",
                )
                if cur.rowcount:
                    cur.execute(f'SET search_path TO {self.schema:s}')
                else:
                    logger.warning('Schema is not exists!!!')
                    self.disconnect()
            else:
                # Get current schema
                cur.execute('SELECT current_schema();')
                default_schema = cur.fetchone()
                # Save default schema as constant, use to list current schema's tables
                self.schema = default_schema[0]
            cur.close()
            return self.connection
        except Exception as e:
            logger.exception(e)
            return False

    def disconnect(self):
        if not self._check_connection():
            return False
        # https://stackoverflow.com/questions/1281875/
        # making-sure-that-psycopg2-database-connection-alive
        self.connection.close()
        self.is_connected = False

    def create_table(self, tblname, colnames):
        if not self._check_connection():
            return False
        sql = f'create table {tblname:s}('
        for idx, val in enumerate(colnames):
            if idx > 0:
                sql += ','
            sql += val['name'] + ' ' + val['type']
        sql += ')'
        logger.info(sql)
        cur = self.connection.cursor()
        cur.execute(sql)
        cur.close()
        self.connection.commit()
        logger.info(f'{tblname} created!')

    # テーブル名を配列として返す
    def list_tables(self):
        if not self._check_connection():
            return []
        sql = 'select table_name from information_schema.tables '
        sql += f"where table_type = 'BASE TABLE' and table_schema = '{self.schema:s}'"
        cur = self.connection.cursor()
        cur.execute(sql)
        # cursor.descriptionはcolumnの配列
        # そこから配列名(column[0])を取り出して配列columnsに代入
        cols = [column[0] for column in cur.description]
        # columnsは取得したカラム名、rowはcolumnsをKeyとして持つ辞書型の配列
        # rowは取得したカラムに対応する値が順番にrow[0], row[1], ...として入っている
        # それをdictでまとめてrowsに取得
        rows = []
        for row in cur.fetchall():
            rows.append(dict(zip(cols, row, strict=False)))
        cur.close()
        # キーに"table_name"を持つ要素を配列として返す
        return [row['table_name'] for row in rows]

