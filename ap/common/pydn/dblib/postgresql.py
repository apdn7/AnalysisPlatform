    # 元はinsert_table関数
    def insert_table_records(self, tblname, names, values, add_comma_to_value=True):
        if not self._check_connection():
            return False

        sql = f'insert into {tblname:s}'

        # Generate column names fields
        sql += '('
        for idx, name in enumerate(names):
            if idx > 0:
                sql += ','
            sql += name
        sql += ') '

        # Generate values field
        if not values:
            return False

        sql += 'values '
        for idx1, value in enumerate(values):
            if idx1 > 0:
                sql += ','
            sql += '('
            for idx2, name in enumerate(names):
                if idx2 > 0:
                    sql += ','

                if value[name] in ('', None):
                    sql += 'Null'
                elif add_comma_to_value:
                    sql += "'" + str(value[name]) + "'"
                else:
                    sql += str(value[name])

            sql += ')'

        cur = self.connection.cursor()
        cur.execute(sql)
        cur.close()
        self.connection.commit()

        logger.info(f'Dummy data was inserted to {tblname}!')

    # SQLをそのまま実行
    # colsとdict形式のrowsを返す
    # cols, rows = db1.run_sql("select * from tbl01")
    # という形で呼び出す
    @log_execution_time('POSTGRES')
    @convert_sa_sql_to_sa_str
    @handle_read_only()
    def run_sql(self, sql: str, row_is_dict=True, params=None):
        if not self._check_connection():
            return False
        cur = self.connection.cursor()
        # https://stackoverflow.com/questions/10252247/
        # how-do-i-get-a-list-of-column-names-from-a-psycopg2-cursor
        # カラム名がRenameされた場合も対応出来る形に処理を変更
        logger.info(sql)
        cur.execute(sql, params)
        # cursor.descriptionはcolumnの配列
        # そこから配列名(column[0])を取り出して配列columnsに代入
        cols = [column[0] for column in cur.description]
        # columnsは取得したカラム名、rowはcolumnsをKeyとして持つ辞書型の配列
        # rowは取得したカラムに対応する値が順番にrow[0], row[1], ...として入っている
        # それをdictでまとめてrowsに取得
        rows = [dict(zip(cols, row, strict=False)) for row in cur.fetchall()] if row_is_dict else cur.fetchall()

        cur.close()
        return cols, rows

    @convert_sa_sql_to_sa_str
    @handle_read_only()
    def fetch_many(self, sql, size=10_000, params=None):
        if not self._check_connection():
            return False

        cur = self.connection.cursor()

        cur.execute(sql, params)

        cols = [column[0] for column in cur.description]
        yield cols
        while True:
            rows = cur.fetchmany(size)
            if not rows:
                break

            yield rows

        cur.close()

    def execute_sql(self, sql):
        """For executing any query requires commit action
        :param sql: SQL to be executed
        :return: Execution result
        """
        if not self._check_connection():
            return False

        cur = self.connection.cursor()
        res = cur.execute(sql)
        cur.close()
        self.connection.commit()

        return res

    # 現時点ではSQLをそのまま実行するだけ
    def get_timezone(self):
        try:
            _, rows = self.run_sql('show timezone')
            tz_offset = str(rows[0]['TimeZone'])
            return tz_offset
        except Exception as e:
            logger.exception(e)
            return None

    # private functions

    def _check_connection(self):
        if self.is_connected:
            return True
        # 接続していないなら
        logger.warning('Connection is not Initialized. Please run connect() to connect to DB')
        return False

    def is_timezone_hold_column(self, tbl, col):
        data_type = self.get_data_type_by_colname(tbl, col)

        return 'WITH TIME ZONE' in data_type.upper()

    @staticmethod
    def gen_sql_and_params(stmt: Union[sa.Select, sa.TextClause]) -> tuple[str, dict[str, str]]:
        compiled_stmt = stmt.compile(dialect=postgresql.dialect(), compile_kwargs={'render_postcompile': True})
        return compiled_stmt.string, compiled_stmt.params
