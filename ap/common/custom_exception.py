from ap.common.constants import MSG_DB_CON_FAILED, UNKNOWN_ERROR_TEXT


class APError(Exception):
    """Base application error.

    This exception serves as the root for application-specific errors and
    carries a human-readable message.

    Args:
        message (str): Error message describing the failure. Defaults to a
            generic value defined by UNKNOWN_ERROR_TEXT.

    Attributes:
        message (str): The message associated with this error instance.

    Note:
        Generated by Junie
    """

    def __init__(self, message=UNKNOWN_ERROR_TEXT) -> None:
        self.message = message
        super().__init__(self, message)


class DatabaseConnectionError(APError):
    """Raised when a database connection cannot be established.

    Specialized APError that annotates which database failed to connect.

    Args:
        dbname (str): The database name or connection identifier that failed.
        message (str): Base message used to build the final error message.
            Defaults to MSG_DB_CON_FAILED.

    Attributes:
        dbname (str): Name or identifier of the database that failed to connect.
        message (str): Final error message composed from ``message`` and
            ``dbname``.

    Note:
        Generated by Junie
    """

    dbname: str

    def __init__(self, dbname, message=MSG_DB_CON_FAILED) -> None:
        self.dbname = dbname
        self.message = f'{message}: {dbname}'
        super().__init__(self.message)
