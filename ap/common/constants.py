class BaseEnum(Enum):
    """Base enumeration class with utility methods.

    Provides common utility methods for working with enum members, including
    retrieval by name, value, and various collection operations.

    Methods:
        __str__: Returns the string representation of the enum name.
        get_items: Returns tuple of all (name, value) pairs.
        get_keys: Returns tuple of all enum member names.
        get_values: Returns tuple of all enum member values.
        get_key: Retrieves enum key by value with optional default.
        get_by_name: Retrieves enum member by name with optional default.
        get_by_enum_value: Retrieves enum member by value with optional default.

    Generated by Duo
    """

    def __str__(self) -> str:
        """Returns the string representation of the enum member.

        Returns:
            str: The name of the enum member.

        Generated by Duo
        """
        return self.name

    @classmethod
    def get_items(cls):
        return tuple(cls.__members__.items())

    @classmethod
    def get_keys(cls):
        return tuple(cls.__members__.keys())

    @classmethod
    def get_values(cls):
        return tuple(cls.__members__.values())

    @classmethod
    def get_key(cls, value, default_key=None):
        for _key, _value in cls.__members__.items():
            if value == _value:
                return _key

        return default_key

    @classmethod
    def get_by_name(cls, name, default=None):
        for _key, _value in cls.__members__.items():
            if _key == name:
                return _key, _value

        return default

    @classmethod
    def get_by_enum_value(cls, enum_value, default=None):
        try:
            # return cls.convert_data_type(enum_value)
            return cls(enum_value)
        except ValueError:
            return default


