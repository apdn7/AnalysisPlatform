class BulkRegisterProcessError(Exception):
    """Exception raised when bulk process registration fails.

    Custom exception for handling errors during bulk process registration
    operations, storing detailed error information for multiple processes.

    Attributes:
        error_data: List of BulkRegisterProcessErrorData containing error details
                   for each failed process registration.

    Methods:
        __str__: Returns formatted error message with all process errors.

    Generated by Duo
    """

    def __init__(self, error_data: list[BulkRegisterProcessErrorData]) -> None:
        """Initializes the bulk register process error exception.

        Args:
            error_data: List of BulkRegisterProcessErrorData containing error details
                       for each failed process registration.

        Generated by Duo
        """
        self.error_data = error_data
        super().__init__()

    def __str__(self) -> str:
        """Returns formatted error message with all process errors.

        Returns:
            str: Semicolon-tab separated string of all process error messages.

        Generated by Duo
        """
        messages: list[str] = []
        for error in self.error_data:
            message = f'{error.process_name} - {error.process_factid} : {error.error_message}'
            messages.append(message)

        return ';\t'.join(messages)


