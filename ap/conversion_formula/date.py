from dataclasses import dataclass
from typing import Self, override

import pandas as pd

from ap.common.constants import FormulaType
from ap.conversion_formula.core import ConversionFormula


@dataclass(frozen=True)
class DateFormula(ConversionFormula):
    """Conversion formula for date strings.

    This class converts a pandas Series of strings into date values using a
    specified strptime-style format string. Invalid parses are coerced to NA.

    Attributes:
        formula: The datetime format string used to parse dates.

    Generated by Junie
    """

    formula: str

    @classmethod
    def from_formula(cls, formula: str) -> Self:
        return cls(formula_type=FormulaType.DATE, formula=formula)

    def validate(self) -> bool:
        # TODO: Implement later
        return True

    def convert(self, series: pd.Series) -> pd.Series:
        return pd.to_datetime(series, errors='coerce', format=self.formula).dt.date.astype(pd.StringDtype())

    def get_formula(self) -> str:
        return self.formula

    @override
    @classmethod
    def detect(cls, series: pd.Series) -> Self | None:
        # TODO: detect date https://gitlab.com/dot-asterisk/biz-app/analysis-interface/analysisinterface/-/issues/221
        return None

    @classmethod
    def revert(cls, series: pd.Series) -> pd.Series:
        return series
