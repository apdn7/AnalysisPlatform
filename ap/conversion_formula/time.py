from dataclasses import dataclass
from typing import Self, override

import pandas as pd

from ap.common.constants import FormulaType
from ap.conversion_formula.core import ConversionFormula


@dataclass(frozen=True)
class TimeFormula(ConversionFormula):
    """Conversion formula for time strings.

    Converts a pandas Series of strings into time values using a provided
    strptime-style time format. Invalid values are coerced to NA.

    Attributes:
        formula: The time format string used for parsing.

    Generated by Junie
    """

    formula: str

    @classmethod
    def from_formula(cls, formula: str) -> Self:
        return cls(formula_type=FormulaType.TIME, formula=formula)

    def validate(self) -> bool:
        # TODO: Implement later
        return True

    def convert(self, series: pd.Series) -> pd.Series:
        return pd.to_datetime(series, errors='coerce', format=self.formula).dt.time.astype(pd.StringDtype())

    def get_formula(self) -> str:
        return self.formula

    @override
    @classmethod
    def detect(cls, series: pd.Series) -> Self | None:
        # TODO: detect date https://gitlab.com/dot-asterisk/biz-app/analysis-interface/analysisinterface/-/issues/221
        return None

    @classmethod
    def revert(cls, series: pd.Series) -> pd.Series:
        return series
