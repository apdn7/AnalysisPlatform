import dataclasses
from abc import ABC, abstractmethod
from typing import Self

import pandas as pd

from ap.common.constants import FormulaType


@dataclasses.dataclass(frozen=True, kw_only=True)
class ConversionFormula(ABC):
    """Abstract base class for data conversion formulas.

    Defines the interface for formulas that convert data during import/export operations.

    Attributes:
        formula_type: Type of the conversion formula.

    Methods:
        from_formula: Constructs a ConversionFormula from a formula string.
        get_formula: Reconstructs the formula as a string.
        validate: Validates whether the formula is valid.
        convert: Applies the formula to convert a pandas Series.
        detect: Auto-detects and creates a formula from data.
        revert: Reverts a converted Series to its original form.

    Generated by Duo.
    """

    formula_type: FormulaType

    @classmethod
    @abstractmethod
    def from_formula(cls, formula: str) -> Self:
        """Construct a `ConversionFormula`"""

    @abstractmethod
    def get_formula(self) -> str:
        """Reconstruct a formula"""

    @abstractmethod
    def validate(self) -> bool:
        """Validate whether this is valid?"""

    @abstractmethod
    def convert(self, series: pd.Series) -> pd.Series:
        """Convert using a formula before importing to db"""

    @classmethod
    @abstractmethod
    def detect(cls, series: pd.Series) -> Self | None:
        """Create formula based on series and column name"""

    @abstractmethod
    def revert(self, series: pd.Series) -> pd.Series:
        """Return 'original' series"""
