class Lookup(BaseFunction):
    """Performs value lookup and replacement with type conversion.

    Maps values from s to corresponding values in t, with optional type specification.

    Attributes:
        s: Comma-separated list of values to match.
        t: Comma-separated list of replacement values (optionally with ::type suffix).

    Methods:
        split_multiple_values_connector: Parses t to extract values and output type.
        custom_validate: Validates that s and t have the same number of values.
        eval_to_series: Performs lookup replacement and type conversion.

    Generated by Duo.
    """

    s: str
    t: str

    def split_multiple_values_connector(self) -> tuple[list[str], str]:
        multi_parts_connector = self.t.split(MULTIPLE_VALUES_CONNECTOR)

        if DOUBLE_COLON_CONNECTOR in multi_parts_connector[-1]:
            # split the string at the first occurrence
            last_part, type_part = multi_parts_connector[-1].split(DOUBLE_COLON_CONNECTOR, 1)
            multi_parts_connector[-1] = last_part
            type_part = type_part if type_part else DataTypeEncode.TEXT.value
        else:
            type_part = DataTypeEncode.TEXT.value

        return multi_parts_connector, type_part

    def custom_validate(self):
        values = self.s.split(MULTIPLE_VALUES_CONNECTOR)
        multi_parts_connector = self.t.split(MULTIPLE_VALUES_CONNECTOR)

        if DOUBLE_COLON_CONNECTOR in multi_parts_connector[-1]:
            t_value, _ = multi_parts_connector[-1].split(DOUBLE_COLON_CONNECTOR, 1)
        else:
            t_value = self.t

        error_msg = (
            f'{self.s} and {t_value} must have the same number of values connected by {MULTIPLE_VALUES_CONNECTOR}'
        )
        replace_values, return_type = self.split_multiple_values_connector()

        if len(values) != len(replace_values):
            raise FunctionFieldError(INVALID_VALUE_MSG).add_error(
                ErrorField(function_type=self.function_type(), field='s', msg=error_msg),
                ErrorField(function_type=self.function_type(), field='t', msg=error_msg),
            )

    @override
    def eval_to_series(
        self,
        *,
        series_x: pd.Series | None = None,
        series_y: pd.Series | None = None,
    ) -> pd.Series | np.NDArray:
        self.custom_validate()

        values = self.s.split(MULTIPLE_VALUES_CONNECTOR)
        replace_values, output_type_cast = self.split_multiple_values_connector()
        dict_replace = dict(zip(values, replace_values, strict=False))
        self.set_type_cast(output_type_cast)
        result = series_x.astype(pd.StringDtype())

        # handle special boolean case
        if pd.api.types.is_bool_dtype(series_x):
            true_keys = [k for k, v in BOOLEAN_DICT_VALUES.items() if v]
            false_keys = [k for k, v in BOOLEAN_DICT_VALUES.items() if not v]

            truth_values = [dict_replace.get(k) for k in true_keys]
            false_values = [dict_replace.get(k) for k in false_keys]

            if truth_values:
                result[series_x] = truth_values[0]
            if false_values:
                result[~series_x] = false_values[0]
        else:
            result = result.replace(dict_replace)

        type_converter = TypeConvert.from_kwargs(t=self.type_cast)
        return type_converter.eval_to_series(series_x=result)


class TypeConvert(BaseFunction):
    """Converts Series data to a specified type.

    Performs type conversion with special handling for boolean, numeric, and string types.

    Attributes:
        t: Target data type (REAL, INTEGER, TEXT, or CATEGORY).

    Methods:
        custom_validate: Validates that t is a supported data type.
        eval_to_series: Converts Series to the specified type.

    Generated by Duo.
    """

    t: str

    def custom_validate(self):
        if self.t is None:
            return None

        required_ts = [
            DataTypeEncode.REAL.value,
            DataTypeEncode.INTEGER.value,
            DataTypeEncode.TEXT.value,
            DataTypeEncode.CATEGORY.value,
        ]
        if self.t.strip() not in required_ts:
            raise FunctionFieldError(INVALID_VALUE_MSG).add_error(
                ErrorField(
                    function_type=self.function_type(),
                    field='t',
                    msg=f'Value of type conversion should be in {required_ts}',
                ),
            )

    @override
    def eval_to_series(
        self,
        *,
        series_x: pd.Series | None = None,
        series_y: pd.Series | None = None,
    ) -> pd.Series | np.NDArray:
        self.custom_validate()

        self.set_type_cast(self.t)

        type_cast = self.get_output_type_cast()
        pd_type = RawDataTypeDB.get_pandas_dtype(type_cast)

        if pd.api.types.is_bool_dtype(series_x):
            if pd.api.types.is_float_dtype(pd_type):
                series_x = series_x.astype('float64')  # Float64 can not astype pd.Series(True, pd.NA, False)
            elif pd_type == pd.StringDtype():
                series_x = series_x.astype(pd_type).str.lower()
            else:
                series_x = series_x.astype(pd_type)

            return series_x

        if pd.api.types.is_numeric_dtype(pd_type):
            casted_x = pd.to_numeric(series_x, errors='coerce')

            if pd.api.types.is_float_dtype(pd_type):
                return casted_x

            # replace float data as NA
            float_index = (np.floor(casted_x) - casted_x) != 0
            casted_x.loc[float_index] = pd.NA

            return casted_x.astype(pd_type)

        # dtype is string
        return series_x.astype(pd_type)


class Shift(BaseFunction):
    """Shifts Series values by a specified number of positions.

    Shifts data forward or backward, filling with a constant value.

    Attributes:
        s: Fill value for shifted positions.
        t: Number of positions to shift (positive=forward, negative=backward).

    Methods:
        custom_validate: Validates that t is an integer.
        eval_to_series: Shifts Series and fills with specified value.

    Generated by Duo.
    """

    s: str
    t: str

    def custom_validate(self):
        try:
            int(self.t)
        except ValueError as e:
            raise FunctionFieldError(INVALID_VALUE_MSG).add_error(
                ErrorField(
                    function_type=self.function_type(),
                    field='t',
                    msg=f'Only accept integer value for t, received `{self.t}` instead',
                ),
            ) from e

    @override
    def eval_to_series(
        self,
        *,
        series_x: pd.Series | None = None,
        series_y: pd.Series | None = None,
    ) -> pd.Series | np.NDArray:
        self.custom_validate()

        s, convertible = cast_value_based_on_series(series_x, self.s)

        t = int(self.t)

        # no need to cast type
        if convertible:
            result = series_x.shift(t, fill_value=s)
        else:
            if isinstance(s, int):
                casted_series = series_x.astype(pd.Int64Dtype())
            elif isinstance(s, float):
                casted_series = series_x.astype(pd.Float64Dtype())
            elif isinstance(s, str):
                casted_series = series_x.astype(pd.StringDtype())
            else:
                raise NotImplementedError(f'Cannot cast `s` of type {type(s)}')

            result = casted_series.shift(t, fill_value=s)

        self.set_type_cast(get_data_encoding_type_from_series(result).value)

        return result


class FillNa(BaseFunction):
    """Fills missing values using various strategies.

    Fills NA values using constant, forward fill, backward fill, or combinations.

    Attributes:
        s: Optional constant value for filling (required if 'c' is in t).
        t: Fill strategy ('c'=constant, 'b'=backfill, 'f'=forward fill, or combinations).

    Methods:
        custom_validate: Validates fill strategy and required parameters.
        eval_to_series: Fills NA values according to the specified strategy.

    Generated by Duo.
    """

    s: str | None = None
    t: str

    def custom_validate(self):
        required_ts = ['c', 'b', 'f', 'bc', 'fc', 'bf', 'fb']
        if self.t.strip() not in required_ts:
            raise FunctionFieldError(INVALID_VALUE_MSG).add_error(
                ErrorField(function_type=self.function_type(), field='t', msg=f'Value of t must in {required_ts}'),
            )

        if 'c' in self.t.strip() and self.s is None:
            raise FunctionFieldError(INVALID_VALUE_MSG).add_error(
                ErrorField(
                    function_type=self.function_type(),
                    field='s',
                    msg='Must provide s when fillna using constant',
                ),
            )

    @override
    def eval_to_series(
        self,
        *,
        series_x: pd.Series | None = None,
        series_y: pd.Series | None = None,
    ) -> pd.Series | np.NDArray:
        self.custom_validate()

        for fill_method in self.t.strip():
            if fill_method == 'b':
                series_x = series_x.fillna(method='ffill')
            elif fill_method == 'f':
                series_x = series_x.fillna(method='bfill')
            elif fill_method == 'c':
                s, convertible = cast_value_based_on_series(series_x, self.s)
                if not convertible:
                    raise FunctionFieldError(INVALID_VALUE_MSG).add_error(
                        ErrorField(
                            function_type=self.function_type(),
                            field='s',
                            msg=f'Cannot cast `s` of type {type(s)}',
                        ),
                    )

                series_x = series_x.fillna(s)

        self.set_type_cast(get_data_encoding_type_from_series(series_x).value)

        return series_x


