class LogTransform(BaseFunction):
    """Logarithmic transformation function: f(x) = a*log10(x-b) + c.

    Applies logarithmic transformation with special handling for negative values.

    Attributes:
        a: Scaling coefficient.
        b: Offset before logarithm.
        c: Intercept constant.

    Methods:
        eval_to_series: Evaluates the log transformation, setting NA for negative inputs.

    Generated by Duo.
    """

    a: float
    b: float
    c: float

    @override
    def eval_to_series(
        self,
        *,
        series_x: pd.Series | None = None,
        series_y: pd.Series | None = None,
    ) -> pd.Series | np.NDArray:
        """
        https://github.com/pandas-dev/pandas/issues/30188
        Calling numpy calculation in Float64 dataframe, can result in np.nan exist in Float64,
        those can cause incorrect behavior, therefore, we need to handle these cases separately
        """
        minus_b = series_x - self.b
        result = self.a * np.log10(minus_b) + self.c

        # log10(x) if x < 0 is NaN
        is_negative = minus_b < 0
        result[is_negative] = pd.NA

        return result


class HexToDec(BaseFunction):
    """Hexadecimal to decimal conversion function.

    Converts hexadecimal string values to decimal integers.

    Methods:
        _convert: Static method to convert a single hex value to decimal.
        eval_to_series: Applies hex-to-decimal conversion to a Series.

    Generated by Duo.
    """

    @staticmethod
    def _convert(value: str | None) -> int | None:
        try:
            return int(value, base=16)
        except (ValueError, TypeError):
            return pd.NA

    @override
    def eval_to_series(
        self,
        *,
        series_x: pd.Series | None = None,
        series_y: pd.Series | None = None,
    ) -> pd.Series | np.NDArray:
        return series_x.apply(self._convert)


class HexToBin(BaseFunction):
    """Hexadecimal to binary conversion function.

    Converts hexadecimal string values to binary string representation.

    Methods:
        _convert: Static method to convert a single hex value to binary string.
        eval_to_series: Applies hex-to-binary conversion to a Series.

    Generated by Duo.
    """

    @staticmethod
    def _convert(value: str | None) -> str | None:
        try:
            return bin(int(value, base=16))[2:]
        except (ValueError, TypeError):
            return pd.NA

    @override
    def eval_to_series(
        self,
        *,
        series_x: pd.Series | None = None,
        series_y: pd.Series | None = None,
    ) -> pd.Series | np.NDArray:
        return series_x.apply(self._convert)


class HexToLogical(BaseFunction):
    """Hexadecimal to logical bit extraction function.

    Extracts a specific bit from a hexadecimal value and returns it as a boolean.

    Attributes:
        n: The bit position to extract (1-indexed).

    Methods:
        eval_to_series: Converts hex to decimal, then extracts the nth bit as boolean.

    Generated by Duo.
    """

    n: int

    @override
    def eval_to_series(
        self,
        *,
        series_x: pd.Series | None = None,
        series_y: pd.Series | None = None,
    ) -> pd.Series | np.NDArray:
        result = HexToDec().eval_to_series(series_x=series_x, series_y=series_y)
        nth_bit = 2 ** (int(self.n) - 1)
        is_na = result.isna()
        result.loc[~is_na] = (result[~is_na] & nth_bit) != 0
        return result


class RadiusPosition(BaseFunction):
    """Calculates radial distance from a reference point.

    Computes the Euclidean distance from point (a, b) scaled by factor c.
    Formula: sqrt((x-a)^2 + (y-b)^2) / c

    Attributes:
        a: X-coordinate of the reference point.
        b: Y-coordinate of the reference point.
        c: Scaling factor for the distance.

    Methods:
        eval_to_series: Calculates radial position from two coordinate Series.

    Generated by Duo.
    """

    a: float
    b: float
    c: float

    @override
    def eval_to_series(
        self,
        *,
        series_x: pd.Series | None = None,
        series_y: pd.Series | None = None,
    ) -> pd.Series | np.NDArray:
        x_minus_a = series_x - self.a
        y_minus_b = series_y - self.b
        return np.sqrt(x_minus_a * x_minus_a + y_minus_b * y_minus_b) / self.c


class AngularPosition(BaseFunction):
    """Calculates angular position relative to a reference point.

    Computes the angle in degrees from point (a, b) with offset c.
    Formula: arctan2(y-b, x-a) converted to degrees, then subtract c.

    Attributes:
        a: X-coordinate of the reference point.
        b: Y-coordinate of the reference point.
        c: Angular offset in degrees.

    Methods:
        eval_to_series: Calculates angular position from two coordinate Series.

    Generated by Duo.
    """

    a: float
    b: float
    c: float

    @override
    def eval_to_series(
        self,
        *,
        series_x: pd.Series | None = None,
        series_y: pd.Series | None = None,
    ) -> pd.Series | np.NDArray:
        result = np.arctan2(series_y - self.b, series_x - self.a)
        result = np.rad2deg(result) - self.c
        return result


class StringExtraction(BaseFunction):
    """Extracts a substring from text data.

    Extracts characters from position n to position k, with type conversion.

    Attributes:
        n: Starting position (1-indexed, negative values count from end).
        k: Ending position (-1 for end of string, negative values count from end).
        t: Output data type (TEXT or CATEGORY).

    Methods:
        custom_validate: Validates that t is TEXT or CATEGORY.
        eval_to_series: Extracts substring and converts to specified type.

    Generated by Duo.
    """

    n: int
    k: int
    t: str

    def custom_validate(self):
        required_ts = [
            DataTypeEncode.TEXT.value,
            DataTypeEncode.CATEGORY.value,
        ]
        if self.t.strip() not in required_ts:
            raise FunctionFieldError(INVALID_VALUE_MSG).add_error(
                ErrorField(function_type=self.function_type(), field='t', msg=f'Value of t must in {required_ts}'),
            )

    @override
    def eval_to_series(
        self,
        *,
        series_x: pd.Series | None = None,
        series_y: pd.Series | None = None,
    ) -> pd.Series | np.NDArray:
        self.custom_validate()
        self.set_type_cast(self.t)

        start = self.n
        if start > 0:
            start -= 1

        stop = None if self.k == -1 else self.k
        if stop is not None and stop < 0:
            stop += 1

        result = series_x.astype(pd.StringDtype()).str.slice(start=start, stop=stop)

        type_converter = TypeConvert.from_kwargs(t=self.type_cast)
