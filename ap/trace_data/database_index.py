import hashlib
from dataclasses import dataclass
from typing import Union

from ap.common.common_utils import gen_sql_cast_text_no_as
from ap.common.constants import RawDataTypeDB


def strip_off_parenthesis(s: str) -> str:
    s = s.strip()
    while s.startswith('(') and s.endswith(')'):
        s = s[1:-1].strip()
    return s


class DBIndex:
    """Abstract base class for database index representations.

    Provides interface for converting between string representations and index objects.

    Methods:
        from_str: Class method to create index from string representation.
        __str__: Convert index to string representation.

    Generated by Duo
    """

    @classmethod
    def from_str(cls, s: str) -> 'DBIndex':
        raise NotImplementedError

    def __str__(self) -> str:
        """
        Return the string representation of the database index.

        This method should be implemented by subclasses to provide a
        human-readable representation suitable for SQL or logging.

        Returns:
            str: String representation of the index.

        Generated by Junie
        """
        raise NotImplementedError


class SingleIndex(DBIndex):
    """Single column database index.

    Represents a simple index on a single column without any transformations.

    Attributes:
        col: Column name for the index.

    Methods:
        from_str: Create SingleIndex from string representation.
        __str__: Convert to string representation.
        __eq__: Check equality with another SingleIndex.
        __hash__: Generate hash for the index.

    Generated by Duo
    """

    def __init__(self, col: str) -> None:
        self.col = col.strip()

    @classmethod
    def from_str(cls, s: str) -> 'SingleIndex':
        s = strip_off_parenthesis(s)
        return cls(s)

    def __str__(self) -> str:
        """Returns string representation of the column name.

        Returns:
            str: The column name.

        Generated by Duo
        """
        return self.col

    def __eq__(self, other: 'SingleIndex') -> bool:
        """Compares equality with another SingleIndex based on string representation.

        Args:
            other: Another SingleIndex instance to compare with.

        Returns:
            bool: True if both indexes have the same string representation, False otherwise.

        Generated by Duo
        """
        return str(self) == str(other)

    def __hash__(self) -> int:
        """Returns hash value based on the column name.

        Returns:
            int: Hash value of the column name.

        Generated by Duo
        """
        return hash(self.col)


class CastTextIndex(DBIndex):
    """Database index with text casting.

    Represents an index that casts a column to text type for indexing.

    Attributes:
        col: Column name to be cast to text.

    Methods:
        from_str: Create index from string, returns SingleIndex or CastTextIndex.
        __str__: Convert to SQL CAST expression string.

    Generated by Duo
    """

    def __init__(self, col: str) -> None:
        self.col = col.strip()

    @classmethod
    def from_str(cls, s: str) -> Union[SingleIndex, 'CastTextIndex']:
        """
        Convert from string
        E.g:
            column_name or (column_name) -> SingleIndex
            CAST(column_name as text) or (CAST(column_name as text)) -> CastTextIndex
        """
        s = strip_off_parenthesis(s)
        if s.startswith('CAST('):
            s = strip_off_parenthesis(s.lstrip('CAST'))
            if s.endswith('AS TEXT'):
                s = strip_off_parenthesis(s.split('AS TEXT')[0])
                return cls(s)
        return SingleIndex.from_str(s)

    def __str__(self) -> str:
        """Returns SQL CAST expression string for text casting.

        Returns:
            str: SQL CAST expression in the format CAST(column AS TEXT).

        Generated by Duo
        """
        return gen_sql_cast_text_no_as(self.col.strip())


class SubstrIndex(DBIndex):
    """Database index with substring extraction.

    Represents an index that extracts a substring from a column for indexing.

    Attributes:
        inner: Inner index (SingleIndex or CastTextIndex).
        substr_from: Starting position for substring extraction.
        substr_to: Ending position for substring extraction.

    Methods:
        from_str: Create index from string representation.
        __str__: Convert to SQL SUBSTR expression string.

    Generated by Duo
    """

    def __init__(self, index: Union[SingleIndex, CastTextIndex], substr_from: int, substr_to: int) -> None:
        self.inner = index
        self.substr_from = substr_from
        self.substr_to = substr_to

    @classmethod
    def from_str(cls, s: str) -> Union['SingleIndex', 'CastTextIndex', 'SubstrIndex']:
        """
        Convert from string
        E.g:
            SUBSTR(column_name, 1, 2), etc -> SubstrIndex
            SUBSTR(CAST(column_name as text), 1, 2) -> SubstrIndex
        """
        s = strip_off_parenthesis(s)
        if not s.startswith('SUBSTR'):
            return CastTextIndex.from_str(s)
        s = s.removeprefix('SUBSTR')

        assert s.startswith('(') and s.endswith(')')
        s = strip_off_parenthesis(s)

        full_col_name, substr_from, distance = s.split(',')
        index = CastTextIndex.from_str(full_col_name)

        return cls(
            index,
            int(substr_from),
            int(substr_from) + int(distance) - 1,
        )

    def __str__(self) -> str:
        """Returns SQL SUBSTR expression string with position and distance.

        Returns:
            str: SQL SUBSTR expression in the format SUBSTR(column, position, distance).

        Generated by Duo
        """
        distance = self.substr_to - self.substr_from + 1
        return f'SUBSTR({self.inner}, {self.substr_from}, {distance})'


class MultipleIndexes(DBIndex):
    """Manages multiple database indexes as a composite index.

    Represents a collection of database indexes that work together as a composite index,
    supporting various index types including single column, cast text, and substring indexes.

    Attributes:
        indexes: List of index objects (SingleIndex, CastTextIndex, or SubstrIndex).

    Methods:
        from_str: Create MultipleIndexes from string representation.
        __str__: Convert to string representation with comma-separated indexes.
        __len__: Get number of indexes in the collection.
        __getitem__: Access individual index by position.
        sort_from: Sort indexes from a starting position.
        to_idx: Generate hashed index name with optional prefix and suffix.
        __hash__: Generate hash for the index collection.
        __eq__: Check equality based on set of index strings.
        __ge__: Check if this index set is superset or equal to another.
        __gt__: Check if this index set is strict superset of another.
        __le__: Check if this index set is subset or equal to another.
        __lt__: Check if this index set is strict subset of another.

    Generated by Duo
    """

    def __init__(self, indexes: list[Union[SingleIndex, CastTextIndex, SubstrIndex]]) -> None:
        if len(indexes) == 0:
            raise ValueError('indexes must not empty')
        self.indexes = indexes

    def __len__(self) -> int:
        """Returns the number of indexes in the collection.

        Returns:
            int: The number of indexes in the collection.

        Generated by Duo
        """
        return len(self.indexes)

    def __getitem__(self, item: int) -> Union[SingleIndex, CastTextIndex, SubstrIndex]:
        """Returns the index at the specified position.

        Args:
            item: The position of the index to retrieve.

        Returns:
            Union[SingleIndex, CastTextIndex, SubstrIndex]: The index at the specified position.

        Generated by Duo
        """
        return self.indexes[item]

    @classmethod
    def from_str(cls, s: str) -> 'MultipleIndexes':
        """
        Convert from str
        E.g:
            (col1, col2)
            (col1, CAST(col2 as text))
            (col1, SUBSTR(col2, 1, 5))
            (col1, SUBSTR(CAST(col2 as text), 1, 5)
            (col1, col2, CAST(col3 as text), SUBSTR(col4, 1, 5), SUBSTR(CAST(col5 as text), 2, 6))
        """
        all_indexes: list[Union[SingleIndex, CastTextIndex, SubstrIndex]] = []

        # remove redundant start string
        s = s[s.find('(') :]

        s = strip_off_parenthesis(s)
        tokens = s.split(',')
        tokens = [token.strip() for token in tokens]

        i = 0
        while i < len(tokens):
            token = strip_off_parenthesis(tokens[i])
            if token.strip(' (').startswith('SUBSTR'):
                string_token = ','.join(tokens[i : i + 3])
                i += 3
            else:
                string_token = tokens[i]
                i += 1
            string_token = strip_off_parenthesis(string_token)
            index = SubstrIndex.from_str(string_token)
            all_indexes.append(index)

        return cls(all_indexes)

    def __str__(self) -> str:
        """Returns comma-separated string representation of all indexes.

        Returns:
            str: Comma-separated indexes enclosed in parentheses.

        Generated by Duo
        """
        index_str = ','.join(str(index) for index in self.indexes)
        return f'({index_str})'

    def sort_from(self, start_index: int):
        self.indexes[start_index:] = sorted(self.indexes[start_index:], key=lambda index: str(index))

    def to_idx(self, prefix: str = '', suffix: str = '') -> str:
        s = f'{prefix}_{self}_{suffix}'.encode('utf-8')
        hashed = hashlib.sha1(s).hexdigest()
        return f'idx_{hashed}'

    def __hash__(self) -> int:
        """Returns hash value based on string representation.

        Returns:
            int: Hash value of the string representation of all indexes.

        Generated by Duo
        """
        return hash(str(self))

    @property
    def __to_set_str(self) -> set[str]:
        return {str(col) for col in self.indexes}

    def __eq__(self, other: 'MultipleIndexes') -> bool:
        """Compares equality based on set of index strings.

        Args:
            other: Another MultipleIndexes instance to compare with.

        Returns:
            bool: True if both have the same set of index strings, False otherwise.

        Generated by Duo
        """
        return self.__to_set_str == other.__to_set_str

    def __ge__(self, other: 'MultipleIndexes') -> bool:
        """Checks if this index set is superset or equal to another.

        Args:
            other: Another MultipleIndexes instance to compare with.

        Returns:
            bool: True if this index set is a superset or equal to the other, False otherwise.

        Generated by Duo
        """
        return self.__to_set_str >= other.__to_set_str

    def __gt__(self, other: 'MultipleIndexes') -> bool:
        """Checks if this index set is strict superset of another.

        Args:
            other: Another MultipleIndexes instance to compare with.

        Returns:
            bool: True if this index set is a strict superset of the other, False otherwise.

        Generated by Duo
        """
        return self.__to_set_str > other.__to_set_str

    def __le__(self, other: 'MultipleIndexes') -> bool:
        """Checks if this index set is subset or equal to another.

        Args:
            other: Another MultipleIndexes instance to compare with.

        Returns:
            bool: True if this index set is a subset or equal to the other, False otherwise.

        Generated by Duo
        """
        return self.__to_set_str <= other.__to_set_str

    def __lt__(self, other: 'MultipleIndexes') -> bool:
        """Checks if this index set is strict subset of another.

        Args:
            other: Another MultipleIndexes instance to compare with.

        Returns:
            bool: True if this index set is a strict subset of the other, False otherwise.

        Generated by Duo
        """
        return self.__to_set_str < other.__to_set_str


@dataclass
class ColumnInfo:
    """Column information for database indexing.

    Dataclass containing metadata about a database column used for creating indexes,
    including support for substring extraction and text casting.

    Attributes:
        bridge_column_name: Name of the bridge column.
        column_type: Type identifier for the column.
        data_type: Data type of the column.
        substr_from: Starting position for substring extraction.
        substr_to: Ending position for substring extraction.

    Methods:
        is_good_key: Check if column has valid substring key configuration.
        is_bad_key: Check if column has invalid key configuration.
        is_substr_key: Check if column is configured as substring key.
        is_text: Check if column data type is text.
        to_substr_index: Convert to SubstrIndex object.
        to_cast_text_index: Convert to CastTextIndex object.
        to_single_index: Convert to SingleIndex object.

    Generated by Duo
    """

    bridge_column_name: str
    column_type: int
    data_type: str
    substr_from: int
    substr_to: int

    def is_good_key(self) -> bool:
        if not self.substr_from and not self.substr_to:
            return False
        return not self.substr_from >= self.substr_to

    def is_bad_key(self) -> bool:
        return not self.is_good_key()

    def is_substr_key(self):
        return self.is_good_key()

    def is_text(self):
        return RawDataTypeDB.is_text_data_type(self.data_type)

    def to_substr_index(self) -> SubstrIndex:
        inner = self.to_cast_text_index() if not self.is_text() else self.to_single_index()
        return SubstrIndex(inner, self.substr_from, self.substr_to)

    def to_cast_text_index(self) -> CastTextIndex:
        return CastTextIndex(self.bridge_column_name)

    def to_single_index(self) -> SingleIndex:
        return SingleIndex(self.bridge_column_name)


def add_multiple_indexes_to_set(
    set_multiple_indexes: set[MultipleIndexes],
    multiple_indexes: MultipleIndexes,
) -> None:
    """Add multiple indexes to the set if it's not subset of any others"""
    if not any(multiple_indexes <= other for other in set_multiple_indexes):
        set_multiple_indexes.add(multiple_indexes)
