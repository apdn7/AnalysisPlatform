from logging import getLogger
from pathlib import Path

import requests

from ap.common.common_utils import WebAuthenticationType
from ap.etl.extract import BaseExtractor

logger = getLogger(__name__)


class WebApiExtractor(BaseExtractor):
    """Extracts data from web APIs with authentication support.

    Supports basic authentication and handles JSON response data.

    Attributes:
        api_url: URL path to the API endpoint.
        username: Optional username for authentication.
        password: Optional password for authentication.
        authentication_type: Type of authentication (NONE or BASIC).

    Methods:
        extract: Fetches data from the API and returns as list of dictionaries.

    Generated by Duo.
    """

    def __init__(
        self,
        api_url: Path,
        username: str | None = None,
        password: str | None = None,
        authentication_type: WebAuthenticationType = WebAuthenticationType.NONE,
    ) -> None:
        self.api_url = api_url
        self.username = username
        self.password = password
        self.authentication_type = authentication_type

    def extract(self, limit: int | None = None) -> list[dict]:
        if self.authentication_type == WebAuthenticationType.BASIC:
            response = requests.get(self.api_url, timeout=10, auth=(self.username, self.password))
        else:
            response = requests.get(self.api_url, timeout=10)
        response.raise_for_status()
        data = response.json()

        if not isinstance(data, list):
            raise ValueError('Invalid json data type, expected list of json object, found: {}', type(data))

        if limit:
            data = data[:limit]
        return data
