class AnyAsBool(fields.Field):
    """Field that serializes string to boolean.

    This custom Marshmallow field converts any value to a boolean representation.

    Methods:
        _deserialize: Converts the input value to a boolean.

    Generated by Duo
    """

    # since _deserialize is an internal method of Field
    # do not overwrite this method in child class
    # use instead:
    #     def deserialize(value):
    #           return bool(value)
    @override
    def _deserialize(
        self,
        value: Any,
        attr: Optional[str],
        data: Optional[Mapping[str, Any]],
        **kwargs: Mapping[str, Any],
    ):
        return bool(value)


class ContextSchemaDict(TypedDict):
    """Type definition for schema context dictionary.

    TypedDict used to provide type hints for context parameters in schema classes.
    Controls behavior of schema serialization, particularly for graph display.

    Attributes:
        show_graph: Boolean flag to control graph display behavior. When True,
            modifies data type conversion and column type handling for visualization.

    Usage:
        Used in BaseSchema and ShowGraphSchema to provide type hints for context parameter.
        Example: ShowGraphSchema(context=ContextSchemaDict(show_graph=True))

    Generated by Duo
    """

    show_graph: bool


