class VisProcessTraceOutSchema(BasePydanticModel):
    """Pydantic schema for visualization process trace output.

    Comprehensive schema for process trace visualization including process details,
    data source information, and trace configurations.

    Attributes:
        id: Process ID.
        name: Process name.
        name_en: English name.
        name_jp: Japanese name.
        name_local: Local name.
        shown_name: Displayed name.
        data_source_id: Data source ID.
        table_name: Database table name.
        master_type: Master type identifier.
        process_factid: Process factory ID.
        etl_func: ETL function name.
        traces: List of trace configurations.
        datalink_tree_directory: Directory path for datalink tree display.
        serial_column_id: ID of the main serial column.

    Methods:
        master: Computed property returning the shown name.
        label: Computed property returning the shown name.
        add_datalink_tree_category: Validator to set datalink tree directory.
        add_serial_column_id: Validator to set serial column ID.

    Generated by Duo
    """

    id: int
    name: str
    name_en: Optional[str]
    name_jp: Optional[str]
    name_local: Optional[str]
    shown_name: str

    data_source_id: int
    table_name: Optional[str]
    master_type: Optional[str]
    process_factid: Optional[str]
    etl_func: Optional[str]
    traces: list[VisTraceOutSchema]
    labels: list[VisLabelOutSchema]

    # generated data
    datalink_tree_directory: str = ''
    serial_column_id: Optional[int] = None

    @pydantic.computed_field
    @property
    def master(self) -> str:
        return self.shown_name

    @pydantic.computed_field
    @property
    def label(self) -> str:
        return self.shown_name

    @pydantic.model_validator(mode='before')
    @classmethod
    def add_datalink_tree_category(cls, data: Any):
        """Group all processes with the same data source id in front-end for viewing in tree directory"""
        # do not validate if this is not cfg process
        if not isinstance(data, CfgProcess):
            return data

        if data.data_source.type == DBType.V2.name:
            data.datalink_tree_directory = data.data_source.csv_detail.directory
        else:
            data.datalink_tree_directory = str(data.data_source_id)

        return data

    @pydantic.model_validator(mode='before')
    @classmethod
    def add_serial_column_id(cls, data: Any):
        """Whether process has main serial column, this use to validate datalink."""
        # do not validate if this is not cfg process
        if not isinstance(data, CfgProcess):
            return data

        serial_column = data.get_main_serial_col() or data.get_main_serial_function_col()
        if serial_column is not None:
            data.serial_column_id = serial_column.id

        return data


