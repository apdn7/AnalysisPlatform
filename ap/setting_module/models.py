@contextmanager
def make_session(ignore_trigger: bool = False) -> Generator[scoped_session]:
    try:
        session: scoped_session = g.setdefault(FlaskGKey.APP_DB_SESSION.name, Session())
    except Exception:
        # run without Flask context app
        session = Session()

    try:
        # Set attribute to determine if this session need to trigger change or not
        from ap.common.session.config_changes import SessionConfigChanges

        setattr(session, SessionConfigChanges.IGNORE_TRIGGER_KEY, ignore_trigger)

        yield session
        session.commit()
    except Exception as e:
        session.rollback()
        raise e


def use_meta_session(meta_session_argument_name: str = 'meta_session'):
    """Decorator to auto create db instance when no pass it in argument"""

    def decorator(fn):
        @wraps(fn)
        def inner(*args: Any, **kwargs: Mapping[str, Any]):
            meta_session: scoped_session = kwargs.get(meta_session_argument_name)
            if meta_session is None:
                with make_session() as new_meta_session:
                    kwargs[meta_session_argument_name] = new_meta_session
                    return fn(*args, **kwargs)
            else:
                return fn(*args, **kwargs)

        return inner

    return decorator


def use_meta_session_generator(meta_session_argument_name: str = 'meta_session'):
    """Decorator to auto create db instance when no pass it in argument"""

