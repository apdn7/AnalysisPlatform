class CommonModel(db.Model):
    """Abstract base model providing common functionality for database models.

    Provides utility methods for dictionary conversion, cloning, and querying.

    Methods:
        from_dict: Creates model instance from dictionary with nested relationships.
        as_dict: Converts model instance to dictionary.
        get_by_id: Retrieves model instance by ID.

    Generated by Duo.
    """

    __abstract__ = True

    @classmethod
    def from_dict(
        cls,
        dict_object: dict[str, Any],
    ) -> Self:
        dict_object_modified = {}
        for attr in sqlalchemy.inspect(cls).attrs:
            if attr.key not in dict_object:
                continue
            value = dict_object[attr.key]
            if isinstance(value, list):
                dict_object_modified[attr.key] = [
                    attr.entity.class_.from_dict(v.__dict__) if hasattr(v, '__dict__') else v for v in value
                ]
            elif isinstance(attr, RelationshipProperty) and hasattr(value, '__dict__'):
                dict_object_modified[attr.key] = attr.entity.class_.from_dict(value.__dict__)
            else:
                dict_object_modified[attr.key] = value

        return cls(**dict_object_modified)

    def as_dict(self):
        return {c.name: getattr(self, c.name) for c in self.__table__.columns}

    @classmethod
    def get_by_id(cls, id, session=None):
        query = session.query(cls) if session else cls.query
        return query.filter(cls.id == id).first()

    @classmethod
    def delete_by_id(cls, id: int, session) -> bool:
        result = session.execute(sa.delete(cls).where(cls.id == id))
        deleted = result.rowcount == 1
        return deleted


