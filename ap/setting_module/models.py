    def get_converted_filter_condition(self):
        """Get normal filter condition if this is a normal column, orthewise, convert it to judge label
        We might add more conditions to this function later
        """
        cfg_column = self.cfg_filter.column

        # normal column
        if not cfg_column.is_judge:
            return self.filter_condition

        formula = conversion_formula(
            formula=cfg_column.formula,
            data_type=cfg_column.data_type,
            col_type=cfg_column.column_type,
        )
        match formula:
            case JudgeFormula(negative_display=negative_display, positive_display=positive_display):
                match self.filter_condition:
                    case '0':
                        return negative_display or JudgeDefinition.NG.name
                    case '1':
                        return positive_display or JudgeDefinition.OK.name
                    case _:
                        return self.filter_condition
            case _:
                raise ValueError(f'Invalid filter condition for judge {self.filter_condition}')


