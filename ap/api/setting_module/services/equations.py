class EquationSampleDataResponse(BaseModel):
    """Pydantic model for equation sample data response.

    This model represents the response from equation sample data calculation,
    containing the calculated sample data and output data type.

    Attributes:
        sample_data: List of calculated sample values of result.
        output_type: Data type of the output (e.g., INTEGER, REAL, TEXT).
        sample_x: List of sampling data of X
        sample_y: List of sampling data of Y
        sample_result: List of sampling data of result

    Generated by Duo.
    """

    sample_data: list[Any]
    output_type: str
    sample_x: list[Any]
    sample_y: list[Any]
    sample_result: list[Any]


class EquationSampleData(BaseModel):
    """Pydantic model for equation sample data input.

    This model represents input data for testing equation calculations,
    including sample X and Y values and equation parameters.

    Attributes:
        data_id: Optional data ID.
        equation_id: Optional equation/function ID.
        var_x: List of X variable sample values.
        var_y: List of Y variable sample values.
        x_data_type: Data type of X variable.
        y_data_type: Data type of Y variable.
        datetime_format: Format string for datetime parsing.
        is_datetime_format: Whether datetime format should be applied.
        a: Equation parameter 'a'.
        b: Equation parameter 'b'.
        c: Equation parameter 'c'.
        n: Equation parameter 'n'.
        k: Equation parameter 'k'.
        s: Equation parameter 's'.
        t: Equation parameter 't'.

    Methods:
        sample_data: Calculate sample data using the equation and return response.

    Generated by Duo.
    """

    data_id: int | None = None
    equation_id: int | None = None
    var_x: list[Any] = Field(default=[], alias='X')
    var_y: list[Any] = Field(default=[], alias='Y')
    x_data_type: str | None = None
    y_data_type: str | None = None
    datetime_format: str | None = None
    is_datetime_format: bool | None = None
    a: str | None = None
    b: str | None = None
    c: str | None = None
    n: str | None = None
    k: str | None = None
    s: str | None = None
    t: str | None = None

    def is_x_number(self) -> bool:
        return self.x_data_type in [RawDataTypeDB.REAL.value, RawDataTypeDB.INTEGER.value]

    def has_y_var(self):
        return self.y_data_type is not None and len(self.var_y) > 0

    def has_only_x(self) -> bool:
        return not self.has_y_var()

    def is_y_number(self) -> bool:
        return self.has_y_var() and self.y_data_type in [RawDataTypeDB.REAL.value, RawDataTypeDB.INTEGER.value]

    def is_x_y_cat(self) -> bool:
        return self.has_y_var() and not self.is_y_number() and not self.is_x_number()

    def is_x_y_number(self) -> bool:
        return self.is_x_number() and self.is_y_number()

    def is_only_x_number(self) -> bool:
        return self.has_only_x() and self.is_x_number()

    def is_only_x_cat(self) -> bool:
        return self.has_only_x() and not self.is_x_number()

    def is_x_cat_y_number(self) -> bool:
        return not self.is_x_number() and self.is_y_number()

    def is_x_number_y_cat(self) -> bool:
        return self.is_x_number() and self.has_y_var() and not self.is_y_number()

    def get_x_var(self):
        return np.array(pd.to_numeric(self.var_x, errors='coerce'))

    def get_y_var(self):
        return np.array(pd.to_numeric(self.var_y, errors='coerce'))

    def format_datetime(self, df):
        datetime_format = self.datetime_format if self.datetime_format is not None and self.is_datetime_format else None
        dic_data_type = {
            ARRAY_X: self.x_data_type,
            ARRAY_Y: self.y_data_type,
        }
        if datetime_format is not None:
            from ap.common.datetime_format_utils import convert_datetime_format

            df = convert_datetime_format(df, dic_data_type, datetime_format)
        return df

    @staticmethod
    def unique_cat_array(
        df_root: pd.DataFrame, column_name: str, sample_rows: int = SAMPLE_FUNCTION_DATA_UNIQUE
    ) -> pd.DataFrame:
        df_cat = (
            df_root.groupby(column_name, dropna=False).sample(n=1, random_state=42).head(sample_rows)
            if df_root[column_name].notna().any()
            else pd.DataFrame([dict.fromkeys(df_root.columns, pd.NA)])
        )
        return df_cat

    def sampling_data(self, df_root) -> pd.DataFrame:
        # case 1 only x is number
        if self.is_only_x_number():
            x_idx = resample_preserve_min_med_max(self.get_x_var(), SAMPLE_FUNCTION_DATA_UNIQUE, True)
            df_root = df_root.iloc[x_idx].drop_duplicates()

        # case 2 only x is category
        elif self.is_only_x_cat():
            df_root = self.unique_cat_array(df_root, ARRAY_X)

        # case 3 x and y are number
        elif self.is_x_y_number():
            x_idx = resample_preserve_min_med_max(self.get_x_var(), SAMPLE_FUNCTION_DATA_UNIQUE, True)
            y_idx = resample_preserve_min_med_max(self.get_y_var(), SAMPLE_FUNCTION_DATA_UNIQUE, True)
            idx = np.union1d(x_idx, y_idx)
            df_root = df_root.iloc[idx].drop_duplicates()

        # case 4 x cat y number
        elif self.is_x_cat_y_number():
            idx_y = resample_preserve_min_med_max(self.get_y_var(), SAMPLE_FUNCTION_DATA_UNIQUE, return_index=True)
            df_num = df_root.iloc[idx_y]
            df_cat = self.unique_cat_array(df_root, ARRAY_X)
            df_root = pd.concat([df_num, df_cat]).drop_duplicates()

        # case 5 x number y cat
        elif self.is_x_number_y_cat():
            idx_x = resample_preserve_min_med_max(self.get_x_var(), SAMPLE_FUNCTION_DATA_UNIQUE, return_index=True)
            df_num = df_root.iloc[idx_x]
            df_cat = self.unique_cat_array(df_root, ARRAY_Y)
            df_root = pd.concat([df_num, df_cat]).drop_duplicates()

        # case 6 x and y are cat
        elif self.is_x_y_cat():
            df_x = self.unique_cat_array(df_root, ARRAY_X)
            df_y = self.unique_cat_array(df_root, ARRAY_Y)
            df_root = pd.concat([df_x, df_y]).drop_duplicates()

        df_root = df_root.reset_index()
        df_root = df_root.drop_duplicates(subset=[ARRAY_X, ARRAY_Y])
        df_root = df_root.replace('', pd.NA)
        df_root = df_root.dropna(subset=[ARRAY_X, ARRAY_Y, SAMPLE_DATA], how='all')

        # sort by requirement
        # 1. 1st row: Both x and y are not NA
        # 2. 2nd row: first record where x is empty and y is not empty
        # 3. 3rf row: first record where x not empty and y is empty
        # 4. 4th row and on: both X and Y are not NA, unique
        priority_rows = pd.concat(
            [
                df_root[df_root[ARRAY_X].notna() & df_root[ARRAY_Y].notna()].head(1),
                df_root[df_root[ARRAY_X].isna() & df_root[ARRAY_Y].notna()].head(1),
                df_root[df_root[ARRAY_X].notna() & df_root[ARRAY_Y].isna()].head(1),
            ]
        )

        df_result = pd.concat([priority_rows, df_root.drop(priority_rows.index)])
        if len(df_result) > SAMPLE_FUNCTION_DATA_UNIQUE:
            df_result = df_result.head(SAMPLE_FUNCTION_DATA_UNIQUE)
        return df_result

    @log_execution_time()
    def sample_data(self) -> EquationSampleDataResponse:
        df_root = pd.DataFrame(
            {
                ARRAY_X: pd.Series(self.var_x),
                ARRAY_Y: pd.Series(self.var_y),
            },
        )

        # Convert datetime format before evaluation
        df = df_root.copy()
        df = self.format_datetime(df)
        equation = get_function_class_by_id(equation_id=self.equation_id).from_kwargs(
            **self.model_dump(),
        )

        evaluate = equation.evaluate(
            df,
            out_col=SAMPLE_DATA,
            x_col=ARRAY_X,
            y_col=ARRAY_Y,
            x_dtype=self.x_data_type,
            y_dtype=self.y_data_type,
        )
        sample_data = evaluate[SAMPLE_DATA]
        output_type = equation.get_output_type(x_data_type=self.x_data_type, y_data_type=self.y_data_type)

        # orjson can't parse infinity
        # https://github.com/ijl/orjson?tab=readme-ov-file#float
        sample_data = sample_data.replace({np.inf: 'inf', -np.inf: '-inf'})
        if pd.api.types.is_bool_dtype(sample_data):
            sample_data = sample_data.astype(pd.StringDtype()).str.lower()

        # sampling data
        df_root[SAMPLE_DATA] = sample_data
        df_result = self.sampling_data(df_root)

        return EquationSampleDataResponse(
            sample_data=sample_data.tolist(),
            output_type=output_type.value,
            sample_x=df_result[ARRAY_X].tolist(),
            sample_y=df_result[ARRAY_Y].tolist(),
            sample_result=df_result[SAMPLE_DATA].tolist(),
        )


def get_all_normal_columns_for_functions(
    function_column_ids: list[int],
    all_columns: list[CfgProcessColumn],
) -> list[int]:
    """
    Suppose we have a scenario like this where 4 and 6 is normal column
    1 -> 2, 3
    2 -> 3, 4
    3 -> 4
    5 -> 6

    If we send in function_column_ids = [1]:
    The required columns are [2, 3, 4]
    @return: list of require columns
    """
    visited: set[int] = set()
    remain_function_column_ids = function_column_ids.copy()

    dict_columns = {column.id: column for column in all_columns}

    answer = []
    while remain_function_column_ids:
        function_column_id = remain_function_column_ids.pop()
        if function_column_id in visited:
            continue
        visited.add(function_column_id)

        column = dict_columns.get(function_column_id)

        if column.id not in function_column_ids:
            answer.append(column.id)

        # this column is function column, we need to continue our search
        for function_detail in column.function_details:
            require_columns = [
                function_detail.var_x,
                function_detail.var_y,
            ]
            remain_function_column_ids.extend(col for col in require_columns if col is not None)

    return answer


def is_all_new_functions(funcs):
    return not any(func[ID] is not None and func[ID] >= 1 for func in funcs)


def remove_all_function_columns(session, proc_id):
    ids = CfgProcessColumn.get_function_col_ids(proc_id)
    if ids:
        CfgProcessColumn.remove_by_col_ids(ids, session)
        CfgProcessFunctionColumn.remove_by_col_ids(ids, session)

    return True


def validate_functions_calculation(
    columns,
    validation_errors: FunctionErrors,
    functions: list[CfgProcessFunctionColumn[str, Any]],
):
    raw_data_types = {cfg_col.id: cfg_col.data_type for cfg_col in columns}
    sample_data_x = [None for _ in range(5)]
    sample_data_y = [None for _ in range(5)]

    for function in functions:
        function_id = function.function_id
        process_column_id = function.process_column_id
        var_x = function.var_x
        var_y = function.var_y

        function_column_id = function.id
        dict_func_col = function.as_dict()
        equation_sample_data = EquationSampleData(
            equation_id=function_id,
            X=sample_data_x,
            Y=sample_data_y,
            x_data_type=raw_data_types.get(var_x),
            y_data_type=raw_data_types.get(var_y),
            **dict_func_col,
        )
        try:
            sample_data = equation_sample_data.sample_data()
            # update data type
            raw_data_types[process_column_id] = sample_data.output_type
        except FunctionFieldError as err:
            validation_errors.add_function_error(err.with_id(function_column_id))


def validate_functions_empty_system_name(validation_errors: FunctionErrors, functions: list[CfgProcessFunctionColumn]):
    invalid_function_column_ids: list[int] = []
    for function in functions:
        process_column = function.cfg_process_column
        name_en = process_column.name_en
        if not name_en:
            invalid_function_column_ids.append(function.id)
    if invalid_function_column_ids:
        validation_errors.add_function_error(
            *(
                FunctionFieldError(
                    id=function_column_id,
                    errors=[ErrorField(field=CfgProcessColumn.name_en.key, msg='System name is empty')],
                )
                for function_column_id in invalid_function_column_ids
            ),
        )


def validate_duplicated_function_name_for_key(
    key: Literal['name_en', 'name_jp', 'name_local'],
    functions: list[CfgProcessFunctionColumn],
    cfg_process_columns: list[CfgProcessColumn],
):
    invalid_function_column_ids: list[int] = []

    normal_name_columns = {getattr(c, key) for c in cfg_process_columns}

    # key: name, value: list of function column ids
    function_name_columns_dict: dict[str, list[int]] = {}
    for function in functions:
        process_column = function.cfg_process_column.as_dict()
        if not process_column:
            continue

        # skip me function
        if function.is_me_function:
            continue

        function_column_id = function.id
        name = process_column.get(key)
        # do not validate empty name
        if not name:
            continue

        if name in normal_name_columns:
            invalid_function_column_ids.append(function_column_id)
        else:
            if name not in function_name_columns_dict:
                function_name_columns_dict[name] = []
            function_name_columns_dict[name].append(function_column_id)

    for duplicated_ids in function_name_columns_dict.values():
        if len(duplicated_ids) > 1:
            invalid_function_column_ids.extend(duplicated_ids)

    return list(set(invalid_function_column_ids))


def validate_functions_duplicated_names(
    cfg_process_columns,
    validation_errors: FunctionErrors,
    functions: list[CfgProcessFunctionColumn],
):
    cfg_process_columns = [cfg_col for cfg_col in cfg_process_columns if not cfg_col.is_generate_equation_column()]

    for key in [CfgProcessColumn.name_en.key, CfgProcessColumn.name_jp.key, CfgProcessColumn.name_local.key]:
        invalid_function_column_ids = validate_duplicated_function_name_for_key(key, functions, cfg_process_columns)
        validation_errors.add_function_error(
            *(
                FunctionFieldError(id=function_column_id, errors=[ErrorField(field=key, msg=f'Duplicated {key}')])
                for function_column_id in invalid_function_column_ids
            ),
        )


def validate_functions_define_by_undefined_columns(
    cfg_process_columns,
    validation_errors: FunctionErrors,
    functions: list[CfgProcessFunctionColumn],
):
    seen_process_column_ids = {col.id for col in cfg_process_columns}
    invalid_function_column_ids = set()

    for function in functions:
        column_id = function.process_column_id
        function_column_id = function.id
        var_x = function.var_x
        var_y = function.var_y

        for id in [var_x, var_y]:
            # column with var_x, var_y that depends on other undefined columns
            # this must be a bug
            if id is not None and id not in seen_process_column_ids:
                invalid_function_column_ids.add(function_column_id)
                break

        seen_process_column_ids.add(column_id)

    if invalid_function_column_ids:
        validation_errors.add_function_error(
            *(
                FunctionFieldError(
                    id=function_column_id,
                    errors=[ErrorField(field=None, msg=_('ErrorMsgFunctionUsesUndefinedColumn'))],
                )
                for function_column_id in invalid_function_column_ids
            ),
        )


def validate_functions(process_columns: list, functions: list[CfgProcessFunctionColumn]):
    validation_errors = FunctionErrors()
    validate_functions_calculation(process_columns, validation_errors, functions)
    validate_functions_empty_system_name(validation_errors, functions)
    validate_functions_duplicated_names(process_columns, validation_errors, functions)
    validate_functions_define_by_undefined_columns(process_columns, validation_errors, functions)

    if validation_errors.has_error():
        raise validation_errors
